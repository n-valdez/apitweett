package com.example.twitterapi.api.bean;

public class ApiTweetResponse extends ApiData {
    private String id;
    private String text;

    public ApiTweetResponse(String id, String text) {
        this.id = id;
        this.text = text;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
